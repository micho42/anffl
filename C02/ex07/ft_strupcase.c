#include <stdbool.h>

char*ft_strupcase(char *str)
{
	int		a;
	char	b;

	a = 0;
	while (true)
	{
		b = str[a];
		if (b == '\0')
			break ;
		if (b >= 'a' && b <= 'z')
		{
			str[a] = b - 32;
		}
		a++;
	}
	return (str);
}
